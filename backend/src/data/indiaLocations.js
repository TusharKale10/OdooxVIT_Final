function listStates(country) {
  const c = TREE[country] || TREE.India;
  return Object.keys(c).sort();
}
function listDistricts(country, state) {
  const s = (TREE[country] || {})[state] || {};
  return Object.keys(s).sort();
}
function listCities(country, state, district) {
  const d = ((TREE[country] || {})[state] || {})[district] || [];
  return [...new Set(d)].sort();
}

